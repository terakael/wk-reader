// Anki comfort scores — optional, generated by scripts/anki_sync.py --with-metadata
// { "学校": { comfort: 0.95, level: 7, kind: "vocabulary" }, ... }
// Falls back gracefully: unreviewed words get a neutral weight.
const comfortPath = join(__dirname, "vocab_comfort.json");
const comfortMap  = new Map();
const reviewsMap  = new Map();
if (existsSync(comfortPath)) {
  const raw = JSON.parse(readFileSync(comfortPath, "utf8"));
  for (const [word, entry] of Object.entries(raw)) {
    if (entry.kind === "radical") continue;  // not useful for story vocab
    comfortMap.set(word, typeof entry === "number" ? entry : entry.comfort);
    if (typeof entry === "object" && entry.reviews !== undefined)
      reviewsMap.set(word, entry.reviews);
  }
  console.log(`Loaded comfort scores for ${comfortMap.size} words.`);
}

// Dimensions come from the index — it's the source of truth for what's stored.
const EMBED_DIM = embIndex.meta.dimensions;
const embVecs   = new Float32Array(embBuf.buffer, embBuf.byteOffset, embBuf.byteLength / 4);

