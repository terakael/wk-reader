// ── Helpers ───────────────────────────────────────────────────────────────────

// Returns a POS-balanced set of vocab items filtered to levels ≤ maxLevel.
// Target mix: ~13 nouns, ~4 suru-nouns, ~7 verbs, ~5 adjectives, ~2 adverbs,
// plus wildcards sampled from ranks 20–100 to fill to 42.
function retrieveVocab(queryVec, maxLevel) {
  const rowCount = embIndex.maxRowByLevel[maxLevel] ?? embIndex.meta.count;
  const scores   = new Array(rowCount);

  for (let r = 0; r < rowCount; r++) {
    const base    = r * EMBED_DIM;
    let dot = 0;
    for (let i = 0; i < EMBED_DIM; i++) dot += queryVec[i] * embVecs[base + i];
    // If comfort data is available: reviewed words get a boost scaled by how
    // much they're struggling (2 - comfort); unreviewed words are heavily
    // suppressed since the user hasn't learned them yet.
    // Without comfort data: neutral weight, level filter is the only gate.
    const comfort = comfortMap.get(embIndex.items[r].character);
    const weight  = comfortMap.size > 0
      ? (comfort !== undefined ? 2 - comfort : 0.1)
      : 1.0;
    scores[r] = { r, dot: dot * weight, pos: posMap.get(r) };
  }
  scores.sort((a, b) => b.dot - a.dot);

  // Bucket by POS (preserving similarity order within each bucket)
  const noun     = scores.filter(s => s.pos === "noun" || s.pos === "other");
  const suruNoun = scores.filter(s => s.pos === "suru-noun");
  const verb     = scores.filter(s => s.pos === "verb");
  const adj      = scores.filter(s => s.pos === "i-adj" || s.pos === "na-adj" || s.pos === "adj");
  const adverb   = scores.filter(s => s.pos === "adverb");

  const picked = new Set();
  const result = [];

  function pickTop(bucket, n) {
    let count = 0;
    for (const s of bucket) {
      if (count >= n) break;
      if (!picked.has(s.r)) {
        picked.add(s.r);
        result.push({ ...embIndex.items[s.r], pos: s.pos });
        count++;
      }
    }
  }

  pickTop(noun,     13);
  pickTop(suruNoun,  4);
  pickTop(verb,      7);
  pickTop(adj,       5);
  pickTop(adverb,    2);

  // Wildcards: random sample from overall ranks 20–100, skip numbers + proper nouns
  const wildcardPool = scores
    .slice(20, 100)
    .filter(s => !picked.has(s.r) && s.pos !== "number");
  while (result.length < 42 && wildcardPool.length > 0) {
    const i = Math.floor(Math.random() * wildcardPool.length);
    const s = wildcardPool.splice(i, 1)[0];
    picked.add(s.r);
    result.push({ ...embIndex.items[s.r], pos: s.pos });
  }

  // Recently-learned injection: tack on up to 2 words reviewed only a handful of
  // times, drawn from the top 150 similarity-ranked candidates so they're at least
  // loosely on-theme. Skipped silently if there aren't enough qualifying words.
  if (reviewsMap.size > 0) {
    const recentPool = scores
      .slice(0, 150)
      .filter(s => !picked.has(s.r)
                && s.pos !== "number"
                && reviewsMap.get(embIndex.items[s.r].character) === 1);
    for (let i = 0; i < 2 && recentPool.length > 0; i++) {
      const idx = Math.floor(Math.random() * Math.min(recentPool.length, 20));
      const s   = recentPool.splice(idx, 1)[0];
      picked.add(s.r);
      result.push({ ...embIndex.items[s.r], pos: s.pos });
    }
  }

  return result;
}

function tokenize(text) {
  return tokenizer.tokenize(text).map(t => {
    let dict = t.basic_form || t.surface_form;
    if (dict.endsWith("な") && /[ぁ-ん]/.test(dict.slice(-2, -1)))
      dict = dict.slice(0, -1) + "い";
    return { surface: t.surface_form, dict, reading: t.reading || "" };
  });
}

function itemsUpToLevel(byLevel, maxLevel) {
  const out = [];
  for (let i = 1; i <= maxLevel; i++)
    for (const item of byLevel[String(i)] || [])
      out.push({ ...item, level: i });
  return out;
}

