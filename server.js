// ── POS tagging ───────────────────────────────────────────────────────────────

function classifyPos(character) {
  const t = tokenizer.tokenize(character)[0];
  if (!t) return "other";
  const { pos, pos_detail_1: pos1 } = t;
  if (pos === "動詞")  return "verb";
  if (pos === "形容詞") return "i-adj";
  if (pos === "副詞")  return "adverb";
  if (pos === "連体詞") return "adj";
  if (pos === "名詞") {
    if (pos1 === "形容動詞語幹") return "na-adj";
    if (pos1 === "サ変接続")    return "suru-noun";
    if (pos1 === "数")           return "number";
    return "noun";
  }
  return "other";
}

const posMap = new Map();
for (const item of embIndex.items) posMap.set(item.row, classifyPos(item.character));

